package CTS_PREPARATION;

public class PositiveorNegative {
    void find(int n){
        if(n<0){
            System.out.println("It's negative");
        }
        else if(n>0){
            System.out.println("It's positive");
        }
        else{
            System.out.println("It's 0");
        }
    }
}
